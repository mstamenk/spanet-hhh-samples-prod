C     for the point in phase space P(0:3,NEXTERNAL)
C     
C     Process: u u > g g u u WEIGHTED<=4
C     Process: c c > g g c c WEIGHTED<=4
C     Process: d d > g g d d WEIGHTED<=4
C     Process: s s > g g s s WEIGHTED<=4
